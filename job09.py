"""Écrire un programme qui récupère la valeur, maximum et le minimum des
éléments de la liste : L = [8, 24, 27, 48, 2,16, 9, 102, 7, 84, 91]."""


# Définir la liste
L = [8, 24, 27, 48, 2, 16, 9, 102, 7, 84, 91]

# Trouver le maximum et le minimum
valeur_max = max(L)
valeur_min = min(L)

# Afficher les résultats
print("Valeur maximale :", valeur_max)
print("Valeur minimale :", valeur_min)
